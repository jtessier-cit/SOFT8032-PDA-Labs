def average5(n1, n2, n3, n4, n5):
    numbers = (n1, n2, n3, n4, n5)
    return sum(numbers) / len(numbers)


def max5(n1, n2, n3, n4, n5):
    numbers = (n1, n2, n3, n4, n5)
    return max(numbers)


def min5(n1, n2, n3, n4, n5):
    numbers = (n1, n2, n3, n4, n5)
    return min(numbers)

def sort5(n1, n2, n3, n4, n5):
    numbers = [n1, n2, n3, n4, n5]
    numbers.sort()
    return numbers


def sum5(n1, n2, n3, n4, n5):
    numbers = (n1, n2, n3, n4, n5)
    return sum(numbers)

